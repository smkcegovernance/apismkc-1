# ?? Network Diagnostic Script for FTP Access
# Run this on your API server to diagnose network routing issues

param(
    [string]$FtpHost = "192.168.40.47",
    [int]$FtpPort = 21,
    [string]$FtpUser = "smkcapi_ftp",
    [string]$FtpPassword = "S0me$tr0ngP@ss!"
)

Write-Host ""
Write-Host "??????????????????????????????????????????????????????????????" -ForegroundColor Cyan
Write-Host "?  FTP Network Diagnostic Tool - SMKCAPI                    ?" -ForegroundColor Cyan
Write-Host "??????????????????????????????????????????????????????????????" -ForegroundColor Cyan
Write-Host ""

Write-Host "???  Running on: $env:COMPUTERNAME" -ForegroundColor Yellow
Write-Host "?? Target FTP: ftp://${FtpHost}:${FtpPort}" -ForegroundColor Yellow
Write-Host ""

# Test 1: Get local IP addresses
Write-Host "??????????????????????????????????????????????????????????" -ForegroundColor DarkGray
Write-Host "Test 1: Local Network Configuration" -ForegroundColor Cyan
Write-Host "??????????????????????????????????????????????????????????" -ForegroundColor DarkGray

$localIPs = Get-NetIPAddress -AddressFamily IPv4 | Where-Object {$_.IPAddress -ne "127.0.0.1"}
Write-Host "Local IPv4 Addresses:" -ForegroundColor Yellow
foreach ($ip in $localIPs) {
    $isTargetNetwork = $ip.IPAddress.StartsWith("192.168.40.")
    $color = if ($isTargetNetwork) { "Green" } else { "White" }
    $indicator = if ($isTargetNetwork) { "?" } else { " " }
    Write-Host "  $indicator $($ip.IPAddress) (Interface: $($ip.InterfaceAlias))" -ForegroundColor $color
}
Write-Host ""

# Test 2: PING test
Write-Host "??????????????????????????????????????????????????????????" -ForegroundColor DarkGray
Write-Host "Test 2: PING Test" -ForegroundColor Cyan
Write-Host "??????????????????????????????????????????????????????????" -ForegroundColor DarkGray

$pingResult = Test-Connection -ComputerName $FtpHost -Count 2 -ErrorAction SilentlyContinue
if ($pingResult) {
    Write-Host "  ? PING successful" -ForegroundColor Green
    Write-Host "    Response time: $($pingResult[0].ResponseTime)ms" -ForegroundColor Gray
} else {
    Write-Host "  ? PING failed" -ForegroundColor Red
    Write-Host "    Issue: Cannot reach FTP server at network level" -ForegroundColor Red
    Write-Host "    Fix: Check network connectivity, routing, or firewall" -ForegroundColor Yellow
}
Write-Host ""

# Test 3: TCP Connection test
Write-Host "??????????????????????????????????????????????????????????" -ForegroundColor DarkGray
Write-Host "Test 3: TCP Port Connection" -ForegroundColor Cyan
Write-Host "??????????????????????????????????????????????????????????" -ForegroundColor DarkGray

$tcpTest = Test-NetConnection -ComputerName $FtpHost -Port $FtpPort -WarningAction SilentlyContinue
if ($tcpTest.TcpTestSucceeded) {
    Write-Host "  ? TCP connection to port $FtpPort successful" -ForegroundColor Green
} else {
    Write-Host "  ? TCP connection to port $FtpPort failed" -ForegroundColor Red
    Write-Host "    Issue: FTP service not running or port blocked" -ForegroundColor Red
    Write-Host "    Fix: Check FTP service status and firewall rules" -ForegroundColor Yellow
}
Write-Host ""

# Test 4: Route trace
Write-Host "??????????????????????????????????????????????????????????" -ForegroundColor DarkGray
Write-Host "Test 4: Network Route Trace" -ForegroundColor Cyan
Write-Host "??????????????????????????????????????????????????????????" -ForegroundColor DarkGray

Write-Host "  Route to $FtpHost :" -ForegroundColor Yellow
$traceResult = Test-NetConnection -ComputerName $FtpHost -TraceRoute -WarningAction SilentlyContinue
if ($traceResult.TraceRoute) {
    foreach ($hop in $traceResult.TraceRoute) {
        Write-Host "    ? $hop" -ForegroundColor Gray
    }
} else {
    Write-Host "    Unable to trace route" -ForegroundColor Red
}
Write-Host ""

# Test 5: Routing table
Write-Host "??????????????????????????????????????????????????????????" -ForegroundColor DarkGray
Write-Host "Test 5: Routing Table" -ForegroundColor Cyan
Write-Host "??????????????????????????????????????????????????????????" -ForegroundColor DarkGray

$routes = route print | Select-String "192.168.40"
if ($routes) {
    Write-Host "  Routes for 192.168.40.x network:" -ForegroundColor Yellow
    foreach ($route in $routes) {
        Write-Host "    $route" -ForegroundColor Gray
    }
} else {
    Write-Host "  ??  No specific routes found for 192.168.40.x" -ForegroundColor Yellow
    Write-Host "    Using default gateway" -ForegroundColor Gray
}
Write-Host ""

# Test 6: Firewall rules
Write-Host "??????????????????????????????????????????????????????????" -ForegroundColor DarkGray
Write-Host "Test 6: Firewall Rules" -ForegroundColor Cyan
Write-Host "??????????????????????????????????????????????????????????" -ForegroundColor DarkGray

$fwProfile = Get-NetFirewallProfile | Where-Object {$_.Enabled -eq $true}
Write-Host "  Active firewall profiles:" -ForegroundColor Yellow
foreach ($profile in $fwProfile) {
    Write-Host "    $($profile.Name): Enabled" -ForegroundColor Gray
}

# Check for FTP-related rules
$ftpRules = Get-NetFirewallRule | Where-Object {
    $_.DisplayName -like "*FTP*" -or 
    $_.DisplayName -like "*192.168.40.47*"
} | Select-Object DisplayName, Direction, Action, Enabled

if ($ftpRules) {
    Write-Host "  FTP-related firewall rules:" -ForegroundColor Yellow
    $ftpRules | Format-Table -AutoSize
} else {
    Write-Host "  ??  No FTP-specific firewall rules found" -ForegroundColor Yellow
}
Write-Host ""

# Test 7: FTP Protocol Test (actual FTP connection)
Write-Host "??????????????????????????????????????????????????????????" -ForegroundColor DarkGray
Write-Host "Test 7: FTP Protocol Test" -ForegroundColor Cyan
Write-Host "??????????????????????????????????????????????????????????" -ForegroundColor DarkGray

try {
    $ftpUri = "ftp://${FtpHost}:${FtpPort}/"
    $request = [System.Net.FtpWebRequest]::Create($ftpUri)
    $request.Method = [System.Net.WebRequestMethods+Ftp]::ListDirectory
    $request.Credentials = New-Object System.Net.NetworkCredential($FtpUser, $FtpPassword)
    $request.UsePassive = $true
    $request.UseBinary = $true
    $request.KeepAlive = $false
    $request.Timeout = 10000

    $response = $request.GetResponse()
    $statusDescription = $response.StatusDescription
    $response.Close()

    Write-Host "  ? FTP connection successful" -ForegroundColor Green
    Write-Host "    Status: $statusDescription" -ForegroundColor Gray
    Write-Host "    User: $FtpUser authenticated" -ForegroundColor Gray
} catch {
    Write-Host "  ? FTP connection failed" -ForegroundColor Red
    Write-Host "    Error: $($_.Exception.Message)" -ForegroundColor Red
    
    if ($_.Exception.InnerException) {
        Write-Host "    Inner: $($_.Exception.InnerException.Message)" -ForegroundColor Red
    }

    $ftpResponse = $_.Exception.InnerException.Response -as [System.Net.FtpWebResponse]
    if ($ftpResponse) {
        Write-Host "    FTP Status: $($ftpResponse.StatusCode) - $($ftpResponse.StatusDescription)" -ForegroundColor Red
    }
}
Write-Host ""

# Test 8: Check IIS App Pool Identity
Write-Host "??????????????????????????????????????????????????????????" -ForegroundColor DarkGray
Write-Host "Test 8: IIS Configuration" -ForegroundColor Cyan
Write-Host "??????????????????????????????????????????????????????????" -ForegroundColor DarkGray

Import-Module WebAdministration -ErrorAction SilentlyContinue
$appPools = Get-ChildItem IIS:\AppPools -ErrorAction SilentlyContinue
if ($appPools) {
    Write-Host "  IIS Application Pools:" -ForegroundColor Yellow
    foreach ($pool in $appPools) {
        $identity = $pool.processModel.identityType
        Write-Host "    $($pool.name): $identity" -ForegroundColor Gray
    }
    Write-Host ""
    Write-Host "  ?? If using ApplicationPoolIdentity, consider changing to NetworkService" -ForegroundColor Yellow
    Write-Host "     for better network access permissions" -ForegroundColor Yellow
} else {
    Write-Host "  ??  Could not load IIS module (run as Administrator)" -ForegroundColor Yellow
}
Write-Host ""

# Summary
Write-Host "??????????????????????????????????????????????????????????????" -ForegroundColor Cyan
Write-Host "?  SUMMARY                                                   ?" -ForegroundColor Cyan
Write-Host "??????????????????????????????????????????????????????????????" -ForegroundColor Cyan
Write-Host ""

$pingOk = $pingResult -ne $null
$tcpOk = $tcpTest.TcpTestSucceeded
$sameNetwork = ($localIPs | Where-Object {$_.IPAddress.StartsWith("192.168.40.")}) -ne $null

Write-Host "  Network Status:" -ForegroundColor Yellow
Write-Host "    Server on 192.168.40.x network: " -NoNewline
if ($sameNetwork) {
    Write-Host "? YES" -ForegroundColor Green
} else {
    Write-Host "? NO (This is the problem!)" -ForegroundColor Red
}

Write-Host "    Can PING FTP server: " -NoNewline
if ($pingOk) {
    Write-Host "? YES" -ForegroundColor Green
} else {
    Write-Host "? NO" -ForegroundColor Red
}

Write-Host "    Can CONNECT to FTP port: " -NoNewline
if ($tcpOk) {
    Write-Host "? YES" -ForegroundColor Green
} else {
    Write-Host "? NO" -ForegroundColor Red
}
Write-Host ""

# Recommendations
Write-Host "  ?? Recommendations:" -ForegroundColor Yellow
Write-Host ""

if (-not $sameNetwork) {
    Write-Host "  ??  CRITICAL: This server is NOT on the 192.168.40.x network!" -ForegroundColor Red
    Write-Host "     This means public requests are being handled by a different server." -ForegroundColor Red
    Write-Host ""
    Write-Host "     Solutions:" -ForegroundColor Yellow
    Write-Host "     1. Update DNS/load balancer to route smkc.gov.in to 192.168.40.35" -ForegroundColor White
    Write-Host "     2. Configure VPN tunnel between this server and 192.168.40.47" -ForegroundColor White
    Write-Host "     3. Move FTP server to a location accessible from both networks" -ForegroundColor White
    Write-Host ""
}

if (-not $pingOk) {
    Write-Host "  ? Cannot ping FTP server" -ForegroundColor Red
    Write-Host ""
    Write-Host "     Possible causes:" -ForegroundColor Yellow
    Write-Host "     - Network routing not configured" -ForegroundColor White
    Write-Host "     - Firewall blocking ICMP" -ForegroundColor White
    Write-Host "     - FTP server is offline" -ForegroundColor White
    Write-Host ""
    Write-Host "     Actions to try:" -ForegroundColor Yellow
    Write-Host "     1. Check if FTP server is powered on and connected" -ForegroundColor White
    Write-Host "     2. Add static route: route add $FtpHost mask 255.255.255.255 <gateway>" -ForegroundColor White
    Write-Host "     3. Check firewall on both servers" -ForegroundColor White
    Write-Host ""
} elseif (-not $tcpOk) {
    Write-Host "  ? Can ping but cannot connect to FTP port" -ForegroundColor Red
    Write-Host ""
    Write-Host "     Possible causes:" -ForegroundColor Yellow
    Write-Host "     - FTP service not running on $FtpHost" -ForegroundColor White
    Write-Host "     - Firewall blocking port 21" -ForegroundColor White
    Write-Host "     - FTP listening on different port" -ForegroundColor White
    Write-Host ""
    Write-Host "     Actions to try:" -ForegroundColor Yellow
    Write-Host "     1. On FTP server, run: Get-Service ftpsvc" -ForegroundColor White
    Write-Host "     2. On FTP server, run: netstat -an | findstr :21" -ForegroundColor White
    Write-Host "     3. Check firewall: New-NetFirewallRule -DisplayName 'FTP Allow' -Direction Inbound -LocalPort 21 -Protocol TCP -Action Allow" -ForegroundColor White
    Write-Host ""
} else {
    Write-Host "  ? Network connectivity to FTP server is OK" -ForegroundColor Green
    Write-Host ""
    Write-Host "     Next step: Run FTP authentication test" -ForegroundColor Yellow
    Write-Host "     1. Via API: curl https://smkc.gov.in:8443/api/ftp-diagnostic/test" -ForegroundColor White
    Write-Host "     2. Manual test: ftp $FtpHost (user: $FtpUser)" -ForegroundColor White
    Write-Host ""
}

# Test 9: Check if running as Administrator
Write-Host "??????????????????????????????????????????????????????????" -ForegroundColor DarkGray
Write-Host "Script Privileges" -ForegroundColor Cyan
Write-Host "??????????????????????????????????????????????????????????" -ForegroundColor DarkGray

$currentPrincipal = New-Object Security.Principal.WindowsPrincipal([Security.Principal.WindowsIdentity]::GetCurrent())
$isAdmin = $currentPrincipal.IsInRole([Security.Principal.WindowsBuiltInRole]::Administrator)

if ($isAdmin) {
    Write-Host "  ? Running as Administrator" -ForegroundColor Green
} else {
    Write-Host "  ??  NOT running as Administrator" -ForegroundColor Yellow
    Write-Host "    Re-run as Administrator for full diagnostics" -ForegroundColor Yellow
}
Write-Host ""

Write-Host "??????????????????????????????????????????????????????????????" -ForegroundColor Cyan
Write-Host "?  END OF DIAGNOSTICS                                        ?" -ForegroundColor Cyan
Write-Host "??????????????????????????????????????????????????????????????" -ForegroundColor Cyan
Write-Host ""

# Save results to file
$reportPath = Join-Path $PSScriptRoot "ftp-diagnostic-report-$(Get-Date -Format 'yyyyMMdd-HHmmss').txt"
$transcript = @"
FTP Network Diagnostic Report
Generated: $(Get-Date -Format 'yyyy-MM-dd HH:mm:ss')
Server: $env:COMPUTERNAME
Target: ftp://${FtpHost}:${FtpPort}

Local IPs:
$($localIPs | ForEach-Object { "  - $($_.IPAddress) ($($_.InterfaceAlias))" } | Out-String)

Results:
  - On 192.168.40.x network: $sameNetwork
  - PING successful: $pingOk
  - TCP connection successful: $tcpOk

Network Route:
$($traceResult.TraceRoute | ForEach-Object { "  ? $_" } | Out-String)
"@

$transcript | Out-File -FilePath $reportPath -Encoding UTF8
Write-Host "?? Report saved to: $reportPath" -ForegroundColor Cyan
Write-Host ""
